/* Relational Operators Example */
#include <stdio.h>
int main()
{
  int a = 111, b = 140;
  printf("a == b: %d \n", a == b);
  printf("a != b: %d \n", a != b);
  printf("a < b: %d \n", a < b);
  printf("a <= b: %d \n", a <= b);
  printf("a > b: %d \n", a > b);
  printf("a >= b: %d \n", a >= b);
  return 0;
}